// Тексты заметок в журнале кассы (cash_log.note) пишутся бэкендом сразу на русском —
// это данные, а не интерфейс, поэтому обычный механизм переводов (tt/tr) их не подхватывает.
// Здесь распознаём известные фиксированные формулировки и заменяем их на перевод,
// сохраняя вставленные в них переменные (курс, имя обменника) как есть.
const FIXED_NOTES = [
  'Продажа (наличные)', 'Продажа (перевод)',
  'Частичная оплата продажи', 'Частичная оплата остатка предзаказа',
  'Остаток предзаказа (наличные)', 'Остаток предзаказа (перевод)',
  'Приход в кассу', 'Расход из кассы',
  'Погашение долга (частично/полностью)',
  'Оплата продажи с баланса', 'Пополнение баланса',
];

export function translateNote(note, tt) {
  if (!note) return note;

  // Точное совпадение с одной из фиксированных формулировок
  const exact = FIXED_NOTES.find(f => f === note);
  if (exact) return tt(exact);

  // "Сдача обменнику" / "Сдача обменнику (Имя)"
  let m = note.match(/^Сдача обменнику(?:\s*\(([^)]*)\))?$/);
  if (m) return m[1] ? `${tt('Сдача обменнику')} (${m[1]})` : tt('Сдача обменнику');

  // "Погашение долга (курс 12.32)" / "Погашение долга клиента (курс 12.32)"
  m = note.match(/^Погашение долга(\s+клиента)?\s*\(курс ([\d.]+)\)$/);
  if (m) return `${tt('Погашение долга')}${m[1] ? ' ' + tt('клиента') : ''} (${tt('курс')} ${m[2]})`;

  return note; // неизвестный текст — оставляем как есть, ничего не ломаем
}
